TWT = function () {
	$.z()
	$.fSb('g', ['u:', $.ipV('un'), $.sb('Get')])
	$.dW('da', [
		$.h3('dt:', $.spT('dt')),
		$.olE('MS', $.liT('ms')),
		$.bt$('Clr', ' $parent.clr')
	])
	ko.b({
		un: $o('@ex'),
		da: $o(),
		g: function () {
			vm.da({
				dt: $D(),
				MS: _.m(['a', 'b'],
						function (t) {
							return {ms: vm.un() + ':' + t}
						})
			})
		},
		clr: function () {
			vm.da()
		}
	})
}
