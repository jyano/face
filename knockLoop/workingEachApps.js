KOE = function () {
	$.x().t([$.tH([$.tr([$.th('F'), $.th('L')])]),
		$.tBE('peep', [
			$.tr([
				$.tdT('f'),
				', ',
				$.tdT('l')
			])
		])])
	ok({
		peep: [
			{f: 'a', l: 'A'},
			{f: 'b', l: 'B'},
			{f: 'c', l: 'C'}
		]
	})
}

BARN = function () {
	$l('barn')
	$.z().ulE(
			'PL', [
				$.li([
					$.sp('Planet: '),
					$.b().bT('n'),
					$.dI('cap', [$.pT('cap.n')])
				])
			])
	ok('PL', [
		{n: 'Merc', cap: null},
		{n: 'Earth', cap: {n: 'Barn'}}
	])
}